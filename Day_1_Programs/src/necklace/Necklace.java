package necklace;

public class Necklace {
	
	private Pearls pearl;
	
	//constructor
	
	public Necklace() {
		super();
	}
	

   
	
	public Necklace(Pearls pearl) {
		super();
		this.pearl = pearl;
	}




	//getter and setter
	
	public Pearls getPearl() {
		return pearl;
	}


	public void setPearl(Pearls pearl) {
		this.pearl = pearl;
	}
	
	

}
