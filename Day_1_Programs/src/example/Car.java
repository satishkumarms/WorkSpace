package example;

public class Car {
	
	private String color;
	private String name;
	
	//constructors
	public Car() { }
	
	
  
	// getters and setters
	
	public Car(String color, String name) {
		super();
		this.color = color;
		this.name = name;
	}



	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
	
	

}
